	private void btnOkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOkActionPerformed
		this.universidade.setAddress(this.fieldEndereco.getText());
		this.universidade.setName(this.fieldNome.getText());
		this.universidade.setUf((Uf)this.comboUF.getSelectedItem());
		this.universityDao.create(this.universidade);
		this.setVisible(false);
		
		this.frameUniversidade.atualizarListaUniversidade();
		this.frameUniversidade.setVisible(true);
	}//GEN-LAST:event_btnOkActionPerformed

